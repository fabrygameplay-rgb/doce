const capítulos = ["ep1","ep2","ep3","ep4","ep5","ep6","ep7","ep10"];
let CapítuloActual = 0;

const previo = document.getElementById("previo");
const próximo = document.getElementById("próximo");
const botones = document.getElementById("botones");

function ShowCap(index) {
    capítulos.forEach(id => {
        const div = document.getElementById(id);
        div.style.display = "none";
    });

    const currentCap = document.getElementById(capítulos[index]);
    currentCap.style.display = "block";

    const videos = currentCap.querySelectorAll("iframe");
    videos.forEach(video => {
        video.classList.remove("animar");
    });

    requestAnimationFrame(() => {
        videos.forEach(video => {
            video.classList.add("animar");
        });
    });

    previo.style.display = index === 0 ? "none" : "inline-block";
    próximo.style.display = index === capítulos.length - 1 ? "none" : "inline-block";

    if (index === 0 || index === capítulos.length - 1) {
        botones.classList.add("centrado");
    } else {
        botones.classList.remove("centrado");
    }
}

ShowCap(CapítuloActual);

previo.addEventListener("click", () => {
    if (CapítuloActual > 0) {
        CapítuloActual--;
        ShowCap(CapítuloActual);
    }
});

próximo.addEventListener("click", () => {
    if(CapítuloActual < capítulos.length - 1) {
        CapítuloActual++;
        ShowCap(CapítuloActual);
    }
});

function actualizarLook(index) {
    sms.classList.remove("animar");
    picture.classList.remove("animar");
     
    void picture.offsetWidth; 
    void sms.offsetWidth; 

    const nuevaFoto = new Image();
    nuevaFoto.src = looks[index].foto;
     
    nuevaFoto.onload = () => {
    picture.src = nuevaFoto.src;
    sms.textContent = looks[index].escrituras;

    requestAnimationFrame(() => {
    requestAnimationFrame(() => {
    sms.classList.add("animar");
    picture.classList.add("animar");
    });
    });
    };
    
    if (nuevaFoto.complete) {
        nuevaFoto.onload();
    }
}

function animarVideos(capIndex) {
    const currentVideos = document.querySelectorAll(`#${capítulos[capIndex]} iframe`);
    currentVideos.forEach(video => {
        video.classList.remove("animar");
        void video.offsetWidth; 
        video.classList.add("animar");
    });
}

